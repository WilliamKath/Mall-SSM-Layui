package org.example.entity;

public class WagesDo {
    private Integer wagesId;

    private String staffName;

    private String wagesJob;

    private Integer wagesBasepay;

    private Integer wagesSubsidy;

    private Integer wagesReward;

    private String wagesNote;

    public Integer getWagesId() {
        return wagesId;
    }

    public void setWagesId(Integer wagesId) {
        this.wagesId = wagesId;
    }

    public String getStaffName() {
        return staffName;
    }

    public void setStaffName(String staffName) {
        this.staffName = staffName;
    }

    public String getWagesJob() {
        return wagesJob;
    }

    public void setWagesJob(String wagesJob) {
        this.wagesJob = wagesJob;
    }

    public Integer getWagesBasepay() {
        return wagesBasepay;
    }

    public void setWagesBasepay(Integer wagesBasepay) {
        this.wagesBasepay = wagesBasepay;
    }

    public Integer getWagesSubsidy() {
        return wagesSubsidy;
    }

    public void setWagesSubsidy(Integer wagesSubsidy) {
        this.wagesSubsidy = wagesSubsidy;
    }

    public Integer getWagesReward() {
        return wagesReward;
    }

    public void setWagesReward(Integer wagesReward) {
        this.wagesReward = wagesReward;
    }

    public String getWagesNote() {
        return wagesNote;
    }

    public void setWagesNote(String wagesNote) {
        this.wagesNote = wagesNote;
    }
}
